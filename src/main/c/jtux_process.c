static long get_max_pathname(const char *path)
{
	long max_path;
	errno = 0;
	max_path = pathconf(path, _PC_PATH_MAX);
	if (max_path == -1) {
		if (errno == 0)
			max_path = 4096; /* guess */
		else
			max_path = 4096; /* bury the error -- return guess here, too */
	}
	return max_path + 1;
}

